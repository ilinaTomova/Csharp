using System;

class DeclareVariables
{
    static void Main()
    {
        ushort firstNumber = 52130;
        sbyte secondNumber = -115;
        int thirdNumber = 4825932;
        byte fourthNumber = 97;
        short fifthNumber = -10000;

        Console.WriteLine(@"{0} is ushort, {1} is sbyte, {2} is int, {3} is byte, {4} is short,", firstNumber, secondNumber, thirdNumber, fourthNumber, fifthNumber);
 }
}

